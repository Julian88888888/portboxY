export const PROFILE_AVAILABLE_FOR_OPTIONS = PROFILE_NICHE_OPTIONS.map((o) => o.name);

export const parseAvailableForSelections = (value) =>
  String(value || '')
    .split(',')
    .map((item) => item.trim())
    .filter(Boolean);

export const formatAvailableForSelections = (selections) =>
  (Array.isArray(selections) ? selections : [])
    .map((item) => {
      if (typeof item === 'string') return item.trim();
      return String(item?.name || item?.id || '').trim();
    })
    .filter(Boolean)
    .join(', ');

export const resolveNicheOptions = (value) => {
  const tokens = parseAvailableForSelections(value);
  return tokens
    .map((token) => {
      const byId = PROFILE_NICHE_OPTIONS.find((o) => o.id === token);
      if (byId) return byId;
      const byName = PROFILE_NICHE_OPTIONS.find(
        (o) => o.name.toLowerCase() === token.toLowerCase()
      );
      if (byName) return byName;
      return { id: token, name: token, group: 'Other' };
    })
    .filter(Boolean);
};
